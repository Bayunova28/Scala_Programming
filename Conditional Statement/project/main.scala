// conditional statement
var salesPrice: Int = 100000
if(salesPrice > 50000) {
  println("Sales Price is very expensive")
}

var spicyLevel: Int = 10
if(spicyLevel > 5) {
  println("Very Spicy")
} else if(spicyLevel == 5) {
  println("Normal")
} else {
  println("Not Spicy")
}

var totalSupply: Int = 500000
if(totalSupply > 500000) {
  println("Very Enough")
} else if(totalSupply == 500000) {
  println("Enough")
} else {
  println("Not Enough")
}
